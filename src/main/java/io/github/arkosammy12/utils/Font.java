package io.github.arkosammy12.utils;

import java.util.Arrays;
import java.util.List;
import java.util.stream.Collectors;

public enum Font {
    ZERO(0x50, 0xF0, 0x90, 0x90, 0x90, 0xF0),
    ONE(0x55, 0x20, 0x60, 0x20, 0x20, 0x70),
    TWO(0x5A, 0xF0, 0x10, 0xF0, 0x80, 0xF0),
    THREE(0x5F, 0xF0, 0x10, 0xF0, 0x10, 0xF0),
    FOUR(0x64, 0x90, 0x90, 0xF0, 0x10, 0x10),
    FIVE(0x69, 0xF0, 0x80, 0xF0, 0x10, 0xF0),
    SIX(0x6E, 0xF0, 0x80, 0xF0, 0x90, 0xF0),
    SEVEN(0x73, 0xF0, 0x10, 0x20, 0x40, 0x40),
    EIGHT(0x78, 0xF0, 0x90, 0xF0, 0x90, 0xF0),
    NINE(0x7D, 0xF0, 0x90, 0xF0, 0x10, 0xF0),
    A(0x82, 0xF0, 0x90, 0xF0, 0x90, 0x90),
    B(0x87, 0xE0, 0x90, 0xE0, 0x90, 0xE0),
    C(0x8C, 0xF0, 0x80, 0x80, 0x80, 0xF0),
    D(0x91, 0xE0, 0x90, 0x90, 0x90, 0xE0),
    E(0x96, 0xF0, 0x80, 0xF0, 0x80, 0xF0),
    F(0x9B, 0xF0, 0x80, 0xF0, 0x80, 0x80);

    Font(int offset, int... bitmap) {
        this.offset = offset;
        this.bitmap = bitmap;
    }

    private final int offset;
    private final int[] bitmap;

    public static List<Font> getFontCharacters() {
        return Arrays.stream(Font.class.getEnumConstants()).collect(Collectors.toList());
    }

    public int[] getBitmap() {
        return this.bitmap;
    }

    public int getOffset() {
        return this.offset;
    }

}