    public StrictChip8Memory(StrictChip8Emulator emulator) {
        super(emulator);
    }

    @Override
    public void loadFont(HexSpriteFont spriteFont) {
        // Loading font data to RAM not required for COSMAC-VIP based CHIP-8 core
    }

    public void setRegister(int index, int value) {
        this.writeByte(REGISTERS_OFFSET + index, value);
    }

    public int getRegister(int index) {
        return this.readByte(REGISTERS_OFFSET + index);
    }

    public void writeStackWord(int index, int value) {
        int stackWordOffset = this.getMemorySize() - 0x130 - index * 2;
        this.writeByte(stackWordOffset - 3, (value & 0xFF00) >>> 8);
        this.writeByte(stackWordOffset - 2, value & 0xFF);
    }

    public int readStackWord(int index) {
        int stackWordOffset = this.getMemorySize() - 0x130 - index * 2;
        return (this.readByte(stackWordOffset - 3) << 8) | this.readByte(stackWordOffset - 2);
    }

    public void drawDisplayPixel(int column, int row) {
        int mask = 0x80 >>> (column & 7);
        int offset = (row << 3) + (column >>> 3);
        int address = DISPLAY_OFFSET + offset;
        this.writeByte(address, this.readByte(address) ^ mask);
    }

    public boolean getDisplayPixel(int column, int row) {
        int mask = 0x80 >>> (column & 7);
        int offset = (row << 3) + (column >>> 3);
        return (this.readByte(DISPLAY_OFFSET + offset) & mask) != 0;
    }

    @Override
    public int readByte(int address) {
        if (address >= 0x8000) {
            return MONITOR_ROM[address & 0x1FF];
        }
        return super.readByte(address);
    }

    @Override
    public void writeByte(int address, int value) {
        if (address >= 0x8000) {
            return;
        }
        super.writeByte(address, value & 0xFF);
    }

    public static int getFontDigitOffset(int hex) {
        return 0x8100 + MONITOR_ROM[0x100 + (hex & 0xF)];
    }

